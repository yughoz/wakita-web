        }
    }

    // update data
    function update($id, $data)
    {
        $this->db->where($this->tbl_contact_id, $id);
        $this->db->update($this->tbl_contact, $data);
    }

    // delete data
    function delete($id)
    {
        $this->db->where($this->tbl_contact_id, $id);
        $this->db->delete($this->tbl_contact);
    }

}

