    // get all
    function get_all()
    {
        $this->db->order_by($this->id, $this->order);
        return $this->db->get($this->tbl_contact)->result();
    }

    // get data by id
    function get_by_id($id)
    {
        $this->db->where($this->tbl_contact_id, $id);
        return $this->db->get($this->tbl_contact)->row();
    }
    
    // get total rows
    function total_rows($q = NULL) {
        $this->db->like($this->tbl_contact_id, $q);
        $this->db->or_like('name_wa', $q);
        $this->db->or_like('name_replace', $q);
        $this->db->or_like('phone', $q);
        $this->db->or_like('group_hotline', $q);
        $this->db->or_like('created', $q);
        $this->db->or_like('createdby', $q);
        $this->db->or_like('updated', $q);
        $this->db->or_like('updatedby', $q);
        $this->db->from($this->tbl_contact);
        return $this->db->count_all_results();
    }

    // get data with limit and search
    function get_limit_data($limit, $start = 0, $q = NULL) {
        $this->db->order_by($this->id, $this->order);
        $this->db->like($this->tbl_contact_id, $q);
        $this->db->or_like('name_wa', $q);
        $this->db->or_like('name_replace', $q);
        $this->db->or_like('phone', $q);
        $this->db->or_like('group_hotline', $q);
        $this->db->or_like('created', $q);
        $this->db->or_like('createdby', $q);
        $this->db->or_like('updated', $q);
        $this->db->or_like('updatedby', $q);
        $this->db->limit($limit, $start);
        return $this->db->get($this->tbl_contact)->result();
    }

    // insert data
    function insert($data)
    {
        try{
            $this->db->insert($this->tbl_contact, $data);
            return true;
        }catch(Exception $e){
